//! ---------------- ES6 therory -------------------

/*
? ECMAScript 2015, also known as ES6, introduced a significant set of new features and syntax improvements to JavaScript, making the language more powerful and expressive. Here's an overview of the key features:

1.  `let` and `const` : Block-scoped variable declarations with `let` and `const` for variables and constants, respectively, providing better scoping and reducing errors compared to `var`.

2.  Arrow Functions : A shorter syntax for writing functions using `=>`, which also lexically binds the `this` value.

3.  Classes : Syntax for defining constructor functions and prototypes more cleanly, using the `class`, `constructor`, `static`, `extends`, and `super` keywords.

4.  Template Literals : Enhanced string literals allowing for multi-line strings and string interpolation with `${expression}` syntax.

5.  Default Parameters : Functions can have default values for parameters, used when no value or `undefined` is passed.

6.  Destructuring Assignment : Syntax that allows unpacking values from arrays or properties from objects into distinct variables.

7.  Rest and Spread Operator : The rest operator (`...`) allows representing an indefinite number of arguments as an array. The spread operator allows expanding an array or object into individual elements or properties.

8.  Enhanced Object Literals : Extended syntax for object literals to support setting the prototype, shorthand for `foo: foo` assignments, defining methods, and making super calls.

9.  Promises : A standard way to handle asynchronous operations, providing an alternative to callback functions.

10.  Modules : Native support for module definition and import/export of variables, functions, or classes, using the `import` and `export` keywords.

11.  `for...of` Loop : A new loop syntax for iterating over iterable objects like arrays, strings, and more, directly accessing the item values.

12.  Set and Map : New collection types for storing unique values (`Set`) and key-value pairs (`Map`).

13.  Symbol : A new primitive data type used to create unique identifiers for object properties.

14.  Generators : Functions that can be exited and later re-entered, keeping the context (variable bindings) across re-entrances.

15.  Proxy : A way to create objects with custom behavior for fundamental operations (e.g., property lookup, assignment, enumeration, function invocation, etc.).

16.  Reflect : A built-in object providing methods for interceptable JavaScript operations, similar to the methods of `Proxy` handlers.

17.  Tail Call Optimization : An optimization in which certain tail recursive functions can be called without growing the call stack.

18.  New Built-in Methods : New methods were added to the existing built-in objects like Array, String, Object, Math, and Number.

20.  Unicode Support : Improved support for Unicode, allowing for the use of astral symbols and the `\n\t{...}` escape syntax.

21.  Internationalization and Localization : New `Intl` object providing language-sensitive string comparison, number formatting, and date and time formatting.

*/